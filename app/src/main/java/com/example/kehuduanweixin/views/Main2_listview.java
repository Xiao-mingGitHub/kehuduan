package com.example.kehuduanweixin.views;

public class Main2_listview {
    private String aName;
    private int aIcon;
    private String mag;

    public Main2_listview(String aName, int aIcon, String mag) {
        this.aName = aName;
        this.aIcon = aIcon;
        this.mag = mag;
    }

    public String getaName() {
        return aName;
    }

    public void setaName(String aName) {
        this.aName = aName;
    }

    public int getaIcon() {
        return aIcon;
    }

    public void setaIcon(int aIcon) {
        this.aIcon = aIcon;
    }

    public String getMag() {
        return mag;
    }

    public void setMag(String mag) {
        this.mag = mag;
    }
}
